// Using + Operator

let num1 = 10;
let num2 = 10;
console.log(num1 + num2);

// Using function

function addNumber(a, b) {
  return a + b;
}
console.log(addNumber(50, 25));

// Using Arrow function

const addnumbers = (a, b) => console.log(a + b);
addnumbers(50, 50);

// Using Addition Assignment (+=) Operator

let A = 10;
let B = 15;
A += B;
console.log(A);
